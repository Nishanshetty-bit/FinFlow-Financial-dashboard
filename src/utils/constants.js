export const fmt = (amount) => {
  return '₹' + Math.abs(amount).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
};

export const fmtDate = (dateStr) => {
  const d = new Date(dateStr + 'T00:00:00');
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' });
};

export const uid = () => {
  return Date.now().toString(36) + Math.random().toString(36).slice(2);
};

export const getMonthKey = (dateStr) => {
  return dateStr.slice(0, 7);
};

export const monthLabel = (key) => {
  const [y, m] = key.split('-');
  return new Date(+y, +m - 1).toLocaleDateString('en-US', { month: 'short', year: '2-digit' });
};

export const computeSummary = (transactions) => {
  const income = transactions.filter(t => t.type === 'income').reduce((s, t) => s + t.amount, 0);
  const expense = transactions.filter(t => t.type === 'expense').reduce((s, t) => s + t.amount, 0);
  const balance = income - expense;
  const savingsRate = income > 0 ? Math.round(((income - expense) / income) * 100) : 0;
  return { income, expense, balance, savingsRate };
};

export const computeCategoryTotals = (transactions, type = 'expense') => {
  const map = {};
  transactions.filter(t => t.type === type).forEach(t => {
    map[t.category] = (map[t.category] || 0) + t.amount;
  });
  return Object.entries(map).sort((a, b) => b[1] - a[1]);
};

export const computeMonthlyData = (transactions, months = 6) => {
  const now = new Date('2026-04-30');
  const keys = [];
  for (let i = months - 1; i >= 0; i--) {
    const d = new Date(now.getFullYear(), now.getMonth() - i, 1);
    keys.push(`${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`);
  }
  const income = {}, expense = {};
  keys.forEach(k => { income[k] = 0; expense[k] = 0; });
  transactions.forEach(t => {
    const k = getMonthKey(t.date);
    if (keys.includes(k)) {
      if (t.type === 'income') income[k] = (income[k] || 0) + t.amount;
      else expense[k] = (expense[k] || 0) + t.amount;
    }
  });
  return { keys, income: keys.map(k => income[k]), expense: keys.map(k => expense[k]) };
};

export const computeBalanceTrend = (transactions, months) => {
  const { keys, income, expense } = computeMonthlyData(transactions, months);
  const labels = keys.map(monthLabel);
  let running = 0;
  const balance = income.map((inc, i) => {
    running += inc - expense[i];
    return +running.toFixed(2);
  });
  return { labels, balance };
};
